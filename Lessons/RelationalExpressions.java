package Lessons;

public class RelationalExpressions {
    public static void main(String[] args) {
        var a = 2;
        var b = 5;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);


    }
}
